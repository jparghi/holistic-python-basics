"""
Title: Tuple Unpacking Demo
Objective: Show how to unpack tuple data into separate variables.
Date: 2024-06-07
"""

contact = ("Skyler", "555-1234", "skyler@example.com")

name, phone, email = contact

print("Contact Details:")
print(f"Name: {name}")
print(f"Phone: {phone}")
print(f"Email: {email}")
